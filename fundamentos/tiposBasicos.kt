package fundamentos

fun main(args: Array<String>){
    val n1: Byte = Byte.MAX_VALUE
    val n2: Short = Short.MAX_VALUE
    val n3: Int = Int.MAX_VALUE
    val n4: Long = Long.MAX_VALUE
    println(n1)
    println(n2)
    println(n3)
    println(n4)

    val n5: Float = 3.14f
    val n6: Double = 3.14
    println(n5)
    println(n6)

    val char: Char = 'A'
    val boolean: Boolean = false
    println(char)
    println(boolean)
}